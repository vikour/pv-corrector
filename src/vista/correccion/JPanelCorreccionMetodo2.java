    @Override
    public List<ConfiguracionCorreccion> getConfiguracionCorreccion() {
        Modulo modulo = curva.getModulo();
        List<ConfiguracionCorreccion> config = new ArrayList<>();
        
        if (jCheckBoxAlpha.isSelected())
            modulo.setAlpha(Double.valueOf(jTextFieldAlpha.getText()));
        
        if (jCheckBoxBeta.isSelected())
            modulo.setBeta(Double.valueOf(jTextFieldBeta.getText()));
        
        if (jCheckBoxRS.isSelected())
            modulo.setRs(Double.valueOf(jTextFieldRS.getText()));
        
        if (jCheckBoxKappa.isSelected())
            modulo.setKappa(Double.valueOf(jTextFieldKappa.getText()));
        
        if (jCheckBoxA.isSelected())
            modulo.setA(Double.valueOf(jTextFieldA.getText()));

        MedidaCanal irradiancia = jPanelCanalIrradiancia.getMedidaCanalElegida();
        MedidaCanal temperatura = jPanelCanalTemperatura.getMedidaCanalElegida();
        double nuevaIrradiancia = jPanelCanalIrradiancia.getNuevoValor();
        double nuevaTemperatura = jPanelCanalTemperatura.getNuevoValor();
      
        ConfiguracionCorreccion correccion = new ConfiguracionCorreccion(irradiancia, temperatura, nuevaIrradiancia, nuevaTemperatura, curva);
        correccion.setAlpha(Double.valueOf(jTextFieldAlpha.getText()));
        correccion.setBeta(Double.valueOf(jTextFieldBeta.getText()));
        correccion.setKappa(Double.valueOf(jTextFieldRS.getText()));
        correccion.setRs(Double.valueOf(jTextFieldKappa.getText()));
        correccion.setA(Double.valueOf(jTextFieldA.getText()));
        config.add(correccion);

        return config;
    }

    @Override
    public void setCurva(CurvaMedida original) {
        Modulo modulo = original.getModulo();
        
        curva = original;
        jPanelCanalIrradiancia.setTituloBorde("Irradiancia");
        jPanelCanalIrradiancia.setCurva(original);
        jPanelCanalTemperatura.setTituloBorde("Temperatura");
        jPanelCanalTemperatura.setCurva(original);
        jTextFieldAlpha.setText(String.valueOf(modulo.getAlpha()));
        jTextFieldBeta.setText(String.valueOf(modulo.getBeta()));
        jTextFieldKappa.setText(String.valueOf(modulo.getKappa()));
        jTextFieldRS.setText(String.valueOf(modulo.getRs()));
        jTextFieldA.setText(String.valueOf(modulo.getA()));
    }


