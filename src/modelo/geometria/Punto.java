
package modelo.geometria;

public class Punto extends ObjetoGeometrico {
    
    private double x;
    private double y;

    public Punto(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "("+ String.format("%.4f",x)+","+String.format("%.4f",y)+")";
    }
    
}
